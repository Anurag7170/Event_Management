import mongoose from "mongoose";

const eventSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: Number,
    required: true,
  },
  eventType: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  budget: {
    type: Number,
    required: true,
  },
});

const Event = mongoose.model("Event", eventSchema);

export default Event;
